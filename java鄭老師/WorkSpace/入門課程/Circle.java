public class Circle {
	public static void main(String[] args) {
	    int  r1 = 10, r2 = 25, r3=50 ; 
		final double PI = 3.14159; 
		System.out.println("半徑為"+r1+"的圓, 面積為" + r1*r1*PI);
		System.out.println("半徑為"+r2+"的圓, 面積為" + r2*r2*PI);
		System.out.println("半徑為"+r3+"的圓, 面積為" + r3*r3*PI);
		

		
	}
} 